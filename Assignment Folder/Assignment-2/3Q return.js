// Write a function `addTwoNumbers` that accepts two numbers as arguments and returns their sum.

function addTwoNumbers(a, b) {
  return a + b;
}

console.log(addTwoNumbers(5, 10)); // Output: 15
console.log(addTwoNumbers(-3, 7)); // Output: 4
console.log(addTwoNumbers(0, 0)); // Output: 0
console.log(addTwoNumbers(1.5, 2.5)); // Output: 4



